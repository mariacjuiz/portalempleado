        $userId = Auth::user()->id;
        $salaries['salaries']=Salary::where('user_id', $userId)
                        ->join('months', 'salaries.month', '=', 'months.id')
                        ->orderBy('year', 'desc')
                        ->orderBy('month', 'asc')
                        ->simplePaginate(10);
